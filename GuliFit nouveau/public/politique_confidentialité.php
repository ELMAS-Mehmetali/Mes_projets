            </ul>

            <h2>2. Données collectées</h2>
            <p>Lors de votre navigation et de votre inscription sur le site, nous pouvons collecter les catégories de données suivantes :</p>
            <ul>
                <li><strong>Informations d’identification :</strong> nom, prénom, adresse email, mot de passe (hashé).</li>
                <li><strong>Profil et préférences :</strong> objectifs sportifs (perte de poids, prise de masse, etc.), type de séance ou de recette, sélection de coach.</li>
                <li><strong>Données de compte :</strong> historique de vos séances, recettes, commentaires ou évaluations.</li>
                <li><strong>Données techniques :</strong> adresse IP, type de navigateur, pages consultées, date et heure de connexion (collectées via cookies et journaux de serveur).</li>
                <li><strong>Données de paiement :</strong> si vous souscrivez à une formule payante, seules les informations nécessaires au traitement de la transaction (nom, coordonnées de facturation) sont partagées avec notre prestataire de paiement (aucune donnée complète de carte bancaire n’est stockée sur nos serveurs).</li>
            </ul>

            <h2>3. Finalités du traitement</h2>
            <p>Nous utilisons vos données pour :</p>
            <ul>
                <li>Créer et gérer votre compte utilisateur, authentifier votre session.</li>
                <li>Vous proposer du contenu personnalisé : séances recommandées, recettes adaptées à vos objectifs, etc.</li>
                <li>Traiter vos achats et abonnements (si applicable) via notre plateforme de paiement sécurisée.</li>
                <li>Améliorer nos services, réaliser des statistiques d’usage et optimiser le parcours utilisateur.</li>
                <li>Gérer la relation client : répondre à vos demandes de contact, assistance, réinitialisation de mot de passe.</li>
                <li>Conserver une trace des échanges et des événements de connexion pour la sécurité du site.</li>
            </ul>

            <h2>4. Bases légales</h2>
            <p>Nous nous appuyons sur les bases légales suivantes pour le traitement de vos données :</p>
            <ul>
                <li><strong>Exécution d’un contrat :</strong> nécessaire à la création et à la gestion de votre compte sur GuliFit, ainsi qu’au traitement de vos commandes.</li>
                <li><strong>Consentement :</strong> pour l’envoi de newsletters, le suivi marketing personnalisé, et les cookies non essentiels.</li>
                <li><strong>Intérêt légitime :</strong> pour la sécurité du site, la prévention des fraudes, et l’amélioration continue de nos services.</li>
                <li><strong>Obligations légales :</strong> conservation de certaines données pour répondre à d’éventuelles obligations fiscales ou légales.</li>
            </ul>

            <h2>5. Cookies et traceurs</h2>
            <p>Nous utilisons des cookies et des technologies similaires pour :</p>
            <ul>
                <li>Assurer le bon fonctionnement du site (cookies strictement nécessaires).</li>
                <li>Mémoriser vos choix de langue, d’affichage et de préférences.</li>
                <li>Analyser les performances du site (Google Analytics, Matomo…).</li>
                <li>Proposer des offres adaptées (ciblage publicitaire, si vous y avez consenti).</li>
            </ul>
            <p>Vous pouvez gérer vos préférences relatives aux cookies à tout moment via notre bannière de cookies ou dans les paramètres de votre navigateur.</p>

            <h2>6. Destinataires des données</h2>
            <p>Vos données peuvent être partagées avec :</p>
            <ul>
                <li>Notre hébergeur et prestataire technique pour garantir la disponibilité et la sécurité du site.</li>
                <li>Notre prestataire de paiement (Stripe, PayPal, etc.) pour le traitement des transactions.</li>
                <li>Nos partenaires marketing (si vous avez exprimé votre consentement) pour vous adresser des offres promotionnelles.</li>
                <li>Les autorités compétentes en cas de réquisition légale ou judiciaire.</li>
            </ul>
            <p>Nous ne vendons ni ne louons vos données personnelles à des tiers.</p>

            <h2>7. Durée de conservation</h2>
            <p>Nous conservons vos données personnelles selon les durées suivantes :</p>
            <ul>
                <li><strong>Compte utilisateur :</strong> tant que votre compte est actif ou pendant 3 ans après votre dernière connexion.</li>
                <li><strong>Données de facturation et transaction :</strong> 10 ans, pour respecter nos obligations fiscales.</li>
                <li><strong>Cookies techniques :</strong> max 13 mois.</li>
                <li><strong>Cookies analytiques/publicitaires :</strong> selon la durée que vous avez autorisée (généralement 6 à 13 mois).</li>
            </ul>

            <h2>8. Sécurité des données</h2>
            <p>Nous mettons en œuvre des mesures techniques et organisationnelles appropriées pour protéger vos données :</p>
            <ul>
                <li>Transmissions chiffrées (HTTPS/TLS).</li>
                <li>Stockage sécurisé (bases de données protégées, accès restreint).</li>
                <li>Hashage des mots de passe via des algorithmes robustes (bcrypt).</li>
                <li>Audit régulier des vulnérabilités et mises à jour système.</li>
            </ul>

            <h2>9. Vos droits</h2>
            <p>Conformément au Règlement Général sur la Protection des Données (RGPD), vous disposez des droits suivants :</p>
            <ul>
                <li><strong>Droit d’accès :</strong> obtenir la copie des données que nous détenons sur vous.</li>
                <li><strong>Droit de rectification :</strong> corriger des données inexactes ou incomplètes.</li>
                <li><strong>Droit à l’effacement (« droit à l’oubli ») :</strong> demander la suppression de vos données (sous réserve des obligations légales de conservation).</li>
                <li><strong>Droit à la limitation du traitement :</strong> demander que l’utilisation de vos données soit limitée.</li>
                <li><strong>Droit d’opposition :</strong> refuser un traitement fondé sur notre intérêt légitime ou un usage marketing.</li>
                <li><strong>Droit à la portabilité :</strong> recevoir un fichier contenant vos données ou les transmettre à un autre prestataire.</li>
                <li><strong>Droit de retirer votre consentement :</strong> à tout moment pour les traitements fondés sur le consentement (newsletter, cookies non essentiels, etc.).</li>
            </ul>
            <p>Pour exercer ces droits, contactez-nous à l’adresse : <a href="mailto:contact@gulifit.com">contact@gulifit.com</a>. Nous nous engageons à répondre dans un délai d’1 mois, conformément à la réglementation.</p>

            <h2>10. Transfert hors Union européenne</h2>
            <p>Si nous transférons vos données vers des prestataires situés en dehors de l’Union européenne, nous nous assurons que :</p>
            <ul>
                <li>Le pays de destination offre un niveau de protection adéquat (décision de la Commission européenne).</li>
                <li>Le transfert est encadré par des garanties standardisées (clauses contractuelles types, BCR, etc.).</li>
            </ul>

            <h2>11. Mineurs</h2>
            <p>Notre site n’est pas destiné aux enfants de moins de 16 ans. Nous ne collectons pas sciemment de données personnelles d’enfants mineurs. Si vous constatez qu’un mineur a fourni des informations sans consentement parental, veuillez nous en informer à <a href="mailto:contact@gulifit.com">contact@gulifit.com</a> pour que nous supprimions ces données.</p>

